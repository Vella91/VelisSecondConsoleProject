package com.methodslecture;

public class Animal {
    public void displayInfo() {
        System.out.println("I am an animal!");
    }

    int age;
    public Animal(){
        this.age = age;
        System.out.println("The animal is of the following age: "  + age);
    }

    String name;
    Animal(String name){
        this.name = name;
        System.out.println("The animal has the following name: " + name);
    }
}
