package com.company;

public class LogicalOperators {
    public static void main(String[] args) {
        boolean first = true;
        boolean second = false;

        System.out.println(first);
        System.out.println(!first);
        System.out.println(first && second);
        System.out.println(first || second);
        System.out.println(!second);
    }
}
